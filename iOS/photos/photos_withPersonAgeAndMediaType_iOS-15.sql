    ZADDITIONALASSETATTRIBUTES.ZREVERSELOCATIONDATA AS 'ZADDITIONALASSETATTRIBUTES.ZREVERSELOCATIONDATA',
    ZASSET.ZTHUMBNAILINDEX AS 'ZASSET.ZTHUMBNAILINDEX',
