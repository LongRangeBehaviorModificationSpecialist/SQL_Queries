SELECT

    ROW_NUMBER() OVER() AS 'RECORD-NUMBER',
    ZLIVEROUTEPERF.Z_PK AS 'LIVE-ROUTE-PERF-Z_PK',
    ZNETWORKATTACHMENT.Z_PK AS 'NETWORK-ATTACHMENT-Z_PK',

    datetime(ZLIVEROUTEPERF.ZTIMESTAMP + 978307200, 'UNIXEPOCH') AS 'TIMESTAMP(UTC)',
    datetime(ZNETWORKATTACHMENT.ZFIRSTTIMESTAMP + 978307200, 'UNIXEPOCH') AS 'FIRST-TIMESTAMP(UTC)',
    datetime(ZNETWORKATTACHMENT.ZTIMESTAMP + 978307200, 'UNIXEPOCH') AS 'TIMESTAMP(UTC)',

    CASE ZLIVEROUTEPERF.ZKIND
        WHEN 1 THEN '1 [WI-FI]'
        WHEN 2 THEN '2 [CELLULAR]'
        ELSE ZLIVEROUTEPERF.ZKIND
    END 'LIVE-ROUTE-PERF-KIND',

    printf("%,d", ZLIVEROUTEPERF.ZBYTESIN) AS 'BYTES-IN',
    printf("%,d", ZLIVEROUTEPERF.ZBYTESOUT) AS 'BYTES-OUT',
    ZLIVEROUTEPERF.ZPACKETSIN AS 'PACKETS-IN',
    ZLIVEROUTEPERF.ZPACKETSOUT AS 'PACKETS-OUT',
    ZLIVEROUTEPERF.ZCONNATTEMPTS AS 'CONN-ATTEMPTS',
    ZLIVEROUTEPERF.ZCONNSUCCESSES AS 'CONN-SUCCESSES',
    ZNETWORKATTACHMENT.ZIDENTIFIER AS 'IDENTIFIER',
    ZNETWORKATTACHMENT.ZISKNOWNGOOD AS 'IS-KNOWN-GOOD',

    hex(ZNETWORKATTACHMENT.ZNETSIGNATURE) AS 'NET-SIGNATURE(HEX)',

    ZNETWORKATTACHMENT.ZOVERALLSTAYMEAN AS 'OVERALL-STAY-MEAN',
    ZNETWORKATTACHMENT.ZOVERALLSTAYVAR AS 'OVERALL-STAY-VAR',
    ZLIVEROUTEPERF.ZOVERALLSTAY AS 'OVERALL-STAY',
    ZLIVEROUTEPERF.ZOVERALLSTAYM2 AS 'OVERALL-STAY-M2',

    /* Source for each line of data */
    'netusage.sqlite; Table: ZZLIVEROUTEPERF(Z_PK:' || ZLIVEROUTEPERF.Z_PK || ')' AS 'DATA-SOURCE'


FROM ZLIVEROUTEPERF
    LEFT JOIN ZNETWORKATTACHMENT ON ZLIVEROUTEPERF.ZHASNETWORKATTACHMENT = ZNETWORKATTACHMENT.Z_PK


ORDER BY
    ZLIVEROUTEPERF.ZTIMESTAMP ASC


