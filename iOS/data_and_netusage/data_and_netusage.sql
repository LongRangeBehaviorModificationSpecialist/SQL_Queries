SELECT

    ROW_NUMBER() OVER() AS 'RECORD-NUMBER',

    CASE
        WHEN ZLIVEUSAGE.ZTIMESTAMP IS NULL THEN '[N/A]'
        ELSE datetime(ZLIVEUSAGE.ZTIMESTAMP + 978307200, 'UNIXEPOCH')
    END AS 'LIVEUSAGE-TIMESTAMP(UTC)',

    datetime(ZPROCESS.ZFIRSTTIMESTAMP + 978307200, 'UNIXEPOCH') AS 'FIRST-TIMESTAMP(UTC)',
    datetime(ZPROCESS.ZTIMESTAMP + 978307200, 'UNIXEPOCH') AS 'PROCESS-TIMESTAMP(UTC)',

    ZPROCESS.ZPROCNAME AS 'PROCESS-NAME',
    ZPROCESS.ZBUNDLENAME AS 'BUNDLE-NAME',
    printf("%,d", ZLIVEUSAGE.ZWIFIIN) AS 'WIFI-BYTES-IN',
    printf("%,d", ZLIVEUSAGE.ZWIFIOUT) AS 'WIFI-BYTES-OUT',
    printf("%,d", ZLIVEUSAGE.ZWWANIN) AS 'WWAN-BYTES-IN',
    printf("%,d", ZLIVEUSAGE.ZWWANOUT) AS 'WWAN-BYTES-OUT',

    /* Source for each line of data */
    'DataUsage.sqlite; Table: ZLIVEUSAGE(Z_PK:' || ZLIVEUSAGE.Z_PK || '), ' || 'Table: ZPROCESS(Z_PK:' || ZPROCESS.Z_PK || ')' AS 'DATA-SOURCE'


FROM ZLIVEUSAGE
    LEFT JOIN ZPROCESS ON ZLIVEUSAGE.ZHASPROCESS = ZPROCESS.Z_PK


WHERE
    ZPROCESS.ZPROCNAME LIKE '%compress%' OR
    ZPROCESS.ZBUNDLENAME LIKE '%compress%'


ORDER BY
    ZLIVEUSAGE.ZTIMESTAMP ASC


